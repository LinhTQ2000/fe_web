import {
  MMSX_ACTIVE_MAINTENANCE_ATTRIBUTE_FAILED,
  MMSX_ACTIVE_MAINTENANCE_ATTRIBUTE_START,
  MMSX_ACTIVE_MAINTENANCE_ATTRIBUTE_SUCCESS,
  MMSX_CREATE_MAINTENANCE_ATTRIBUTE_FAILED,
  MMSX_CREATE_MAINTENANCE_ATTRIBUTE_START,
  MMSX_CREATE_MAINTENANCE_ATTRIBUTE_SUCCESS,
  MMSX_GET_DETAIL_MAINTENANCE_ATTRIBUTE_FAILED,
  MMSX_GET_DETAIL_MAINTENANCE_ATTRIBUTE_START,
  MMSX_GET_DETAIL_MAINTENANCE_ATTRIBUTE_SUCCESS,
  MMSX_IN_ACTIVE_MAINTENANCE_ATTRIBUTE_FAILED,
  MMSX_IN_ACTIVE_MAINTENANCE_ATTRIBUTE_START,
  MMSX_IN_ACTIVE_MAINTENANCE_ATTRIBUTE_SUCCESS,
  MMSX_RESET_MAINTENANCE_ATTRIBUTE_STATE,
  MMSX_SEARCH_MAINTENANCE_ATTRIBUTE_FAILED,
  MMSX_SEARCH_MAINTENANCE_ATTRIBUTE_START,
  MMSX_SEARCH_MAINTENANCE_ATTRIBUTE_SUCCESS,
  MMSX_UPDATE_MAINTENANCE_ATTRIBUTE_FAILED,
  MMSX_UPDATE_MAINTENANCE_ATTRIBUTE_START,
  MMSX_UPDATE_MAINTENANCE_ATTRIBUTE_SUCCESS,
} from '../actions/maintenance-attribute'

const initialState = {
  isLoading: false,
  maintenanceAttributeList: [],
  maintenanceAttributeDetails: {},
  total: null,
}

export default function maintenanceAttribute(state = initialState, action) {
  switch (action.type) {
    case MMSX_SEARCH_MAINTENANCE_ATTRIBUTE_START:
    case MMSX_GET_DETAIL_MAINTENANCE_ATTRIBUTE_START:
    case MMSX_ACTIVE_MAINTENANCE_ATTRIBUTE_START:
    case MMSX_IN_ACTIVE_MAINTENANCE_ATTRIBUTE_START:
    case MMSX_CREATE_MAINTENANCE_ATTRIBUTE_START:
    case MMSX_UPDATE_MAINTENANCE_ATTRIBUTE_START:
      return {
        ...state,
        isLoading: true,
      }
    case MMSX_SEARCH_MAINTENANCE_ATTRIBUTE_FAILED:
    case MMSX_GET_DETAIL_MAINTENANCE_ATTRIBUTE_FAILED:
    case MMSX_ACTIVE_MAINTENANCE_ATTRIBUTE_SUCCESS:
    case MMSX_ACTIVE_MAINTENANCE_ATTRIBUTE_FAILED:
    case MMSX_IN_ACTIVE_MAINTENANCE_ATTRIBUTE_SUCCESS:
    case MMSX_IN_ACTIVE_MAINTENANCE_ATTRIBUTE_FAILED:
    case MMSX_CREATE_MAINTENANCE_ATTRIBUTE_SUCCESS:
    case MMSX_CREATE_MAINTENANCE_ATTRIBUTE_FAILED:
    case MMSX_UPDATE_MAINTENANCE_ATTRIBUTE_SUCCESS:
    case MMSX_UPDATE_MAINTENANCE_ATTRIBUTE_FAILED:
      return {
        ...state,
        isLoading: false,
      }
    case MMSX_SEARCH_MAINTENANCE_ATTRIBUTE_SUCCESS:
      return {
        ...state,
        maintenanceAttributeList: action.payload.list,
        isLoading: false,
        total: action.payload.total,
      }
    case MMSX_GET_DETAIL_MAINTENANCE_ATTRIBUTE_SUCCESS:
      return {
        ...state,
        maintenanceAttributeDetails: action.payload,
        isLoading: false,
      }
    case MMSX_RESET_MAINTENANCE_ATTRIBUTE_STATE:
      return {
        ...state,
        maintenanceAttributeDetails: {},
      }
    default:
      return state
  }
}
