import {
  CONFIRM_WAREHOUSE_IMPORT_TICKET_FAIL,
  CONFIRM_WAREHOUSE_IMPORT_TICKET_START,
  CONFIRM_WAREHOUSE_IMPORT_TICKET_SUCCESS,
  GET_WAREHOUSE_IMPORT_TICKET_DETAIL_FAIL,
  GET_WAREHOUSE_IMPORT_TICKET_DETAIL_START,
  GET_WAREHOUSE_IMPORT_TICKET_DETAIL_SUCCESS,
  GET_WAREHOUSE_IMPORT_TICKET_LIST_FAIL,
  GET_WAREHOUSE_IMPORT_TICKET_LIST_START,
  GET_WAREHOUSE_IMPORT_TICKET_LIST_SUCCESS,
  REJECT_WAREHOUSE_IMPORT_TICKET_FAIL,
  REJECT_WAREHOUSE_IMPORT_TICKET_START,
  REJECT_WAREHOUSE_IMPORT_TICKET_SUCCESS,
  CREATE_WAREHOUSE_IMPORT_TICKET,
  CREATE_WAREHOUSE_IMPORT_TICKET_SUCCESS,
  CREATE_WAREHOUSE_IMPORT_TICKET_FAIL,
  UPDATE_WAREHOUSE_IMPORT_TICKET,
  UPDATE_WAREHOUSE_IMPORT_TICKET_FAIL,
  UPDATE_WAREHOUSE_IMPORT_TICKET_SUCCESS,
  DELETE_WAREHOUSE_IMPORT_TICKET_START,
  DELETE_WAREHOUSE_IMPORT_TICKET_FAIL,
  DELETE_WAREHOUSE_IMPORT_TICKET_SUCCESS,
  RESET_STATE_WAREHOUSE_IMPORT_TICKET,
} from '../actions/warehouse-import-management'

const initialState = {
  warehouseImportManagementList: [],
  warehouseImportManagementDetail: {},
  isLoading: false,
  total: null,
}

export default function warehouseImportManagement(
  state = initialState,
  action,
) {
  switch (action.type) {
    case GET_WAREHOUSE_IMPORT_TICKET_LIST_START:
    case CONFIRM_WAREHOUSE_IMPORT_TICKET_START:
    case REJECT_WAREHOUSE_IMPORT_TICKET_START:
    case GET_WAREHOUSE_IMPORT_TICKET_DETAIL_START:
    case CREATE_WAREHOUSE_IMPORT_TICKET:
    case UPDATE_WAREHOUSE_IMPORT_TICKET:
    case DELETE_WAREHOUSE_IMPORT_TICKET_START:
      return {
        ...state,
        isLoading: true,
      }
    case GET_WAREHOUSE_IMPORT_TICKET_LIST_SUCCESS:
      return {
        ...state,
        isLoading: false,
        warehouseImportManagementList: action.payload.items,
        total: action?.payload?.meta?.total,
      }
    case GET_WAREHOUSE_IMPORT_TICKET_DETAIL_SUCCESS:
      return {
        ...state,
        isLoading: false,
        warehouseImportManagementDetail: action.payload,
      }
    case GET_WAREHOUSE_IMPORT_TICKET_LIST_FAIL:
    case GET_WAREHOUSE_IMPORT_TICKET_DETAIL_FAIL:
    case CONFIRM_WAREHOUSE_IMPORT_TICKET_SUCCESS:
    case CONFIRM_WAREHOUSE_IMPORT_TICKET_FAIL:
    case REJECT_WAREHOUSE_IMPORT_TICKET_SUCCESS:
    case REJECT_WAREHOUSE_IMPORT_TICKET_FAIL:
    case CREATE_WAREHOUSE_IMPORT_TICKET_SUCCESS:
    case CREATE_WAREHOUSE_IMPORT_TICKET_FAIL:
    case UPDATE_WAREHOUSE_IMPORT_TICKET_FAIL:
    case UPDATE_WAREHOUSE_IMPORT_TICKET_SUCCESS:
    case DELETE_WAREHOUSE_IMPORT_TICKET_SUCCESS:
    case DELETE_WAREHOUSE_IMPORT_TICKET_FAIL:
      return {
        ...state,
        isLoading: false,
      }
    case RESET_STATE_WAREHOUSE_IMPORT_TICKET:
      return {
        ...state,
        warehouseImportManagementDetail: {},
      }
    default:
      return state
  }
}
