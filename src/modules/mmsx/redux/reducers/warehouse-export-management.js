import {
  CONFIRM_WAREHOUSE_EXPORT_REQUEST_FAIL,
  CONFIRM_WAREHOUSE_EXPORT_REQUEST_START,
  CONFIRM_WAREHOUSE_EXPORT_REQUEST_SUCCESS,
  GET_WAREHOUSE_EXPORT_REQUEST_DETAIL_FAIL,
  GET_WAREHOUSE_EXPORT_REQUEST_DETAIL_START,
  GET_WAREHOUSE_EXPORT_REQUEST_DETAIL_SUCCESS,
  GET_WAREHOUSE_EXPORT_REQUEST_LIST_FAIL,
  GET_WAREHOUSE_EXPORT_REQUEST_LIST_START,
  GET_WAREHOUSE_EXPORT_REQUEST_LIST_SUCCESS,
  REJECT_WAREHOUSE_EXPORT_REQUEST_FAIL,
  REJECT_WAREHOUSE_EXPORT_REQUEST_START,
  REJECT_WAREHOUSE_EXPORT_REQUEST_SUCCESS,
  CREATE_WAREHOUSE_EXPORT_REQUEST,
  CREATE_WAREHOUSE_EXPORT_REQUEST_SUCCESS,
  CREATE_WAREHOUSE_EXPORT_REQUEST_FAIL,
  UPDATE_WAREHOUSE_EXPORT_REQUEST,
  UPDATE_WAREHOUSE_EXPORT_REQUEST_FAIL,
  UPDATE_WAREHOUSE_EXPORT_REQUEST_SUCCESS,
  DELETE_WAREHOUSE_EXPORT_REQUEST_START,
  DELETE_WAREHOUSE_EXPORT_REQUEST_FAIL,
  DELETE_WAREHOUSE_EXPORT_REQUEST_SUCCESS,
  RESET_STATE_WAREHOUSE_EXPORT_REQUEST,
} from '../actions/warehouse-export-management'

const initialState = {
  warehouseExportManagementList: [],
  warehouseExportManagementDetail: {},
  isLoading: false,
  total: null,
}

export default function warehouseExportManagement(
  state = initialState,
  action,
) {
  switch (action.type) {
    case GET_WAREHOUSE_EXPORT_REQUEST_LIST_START:
    case CONFIRM_WAREHOUSE_EXPORT_REQUEST_START:
    case REJECT_WAREHOUSE_EXPORT_REQUEST_START:
    case GET_WAREHOUSE_EXPORT_REQUEST_DETAIL_START:
    case CREATE_WAREHOUSE_EXPORT_REQUEST:
    case UPDATE_WAREHOUSE_EXPORT_REQUEST:
    case DELETE_WAREHOUSE_EXPORT_REQUEST_START:
      return {
        ...state,
        isLoading: true,
      }
    case GET_WAREHOUSE_EXPORT_REQUEST_LIST_SUCCESS:
      return {
        ...state,
        isLoading: false,
        warehouseExportManagementList: action.payload.items,
        total: action?.payload?.meta?.total,
      }
    case GET_WAREHOUSE_EXPORT_REQUEST_DETAIL_SUCCESS:
      return {
        ...state,
        isLoading: false,
        warehouseExportManagementDetail: action.payload,
      }
    case GET_WAREHOUSE_EXPORT_REQUEST_LIST_FAIL:
    case GET_WAREHOUSE_EXPORT_REQUEST_DETAIL_FAIL:
    case CONFIRM_WAREHOUSE_EXPORT_REQUEST_SUCCESS:
    case CONFIRM_WAREHOUSE_EXPORT_REQUEST_FAIL:
    case REJECT_WAREHOUSE_EXPORT_REQUEST_SUCCESS:
    case REJECT_WAREHOUSE_EXPORT_REQUEST_FAIL:
    case CREATE_WAREHOUSE_EXPORT_REQUEST_SUCCESS:
    case CREATE_WAREHOUSE_EXPORT_REQUEST_FAIL:
    case UPDATE_WAREHOUSE_EXPORT_REQUEST_FAIL:
    case UPDATE_WAREHOUSE_EXPORT_REQUEST_SUCCESS:
    case DELETE_WAREHOUSE_EXPORT_REQUEST_SUCCESS:
    case DELETE_WAREHOUSE_EXPORT_REQUEST_FAIL:
      return {
        ...state,
        isLoading: false,
      }
    case RESET_STATE_WAREHOUSE_EXPORT_REQUEST:
      return {
        ...state,
        warehouseExportManagementDetail: {},
      }
    default:
      return state
  }
}
