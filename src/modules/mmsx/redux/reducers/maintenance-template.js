import {
  MMSX_ACTIVE_MAINTENANCE_TEMPLATE_FAILED,
  MMSX_ACTIVE_MAINTENANCE_TEMPLATE_START,
  MMSX_ACTIVE_MAINTENANCE_TEMPLATE_SUCCESS,
  MMSX_CREATE_MAINTENANCE_TEMPLATE_FAILED,
  MMSX_CREATE_MAINTENANCE_TEMPLATE_START,
  MMSX_CREATE_MAINTENANCE_TEMPLATE_SUCCESS,
  MMSX_GET_DETAIL_MAINTENANCE_TEMPLATE_FAILED,
  MMSX_GET_DETAIL_MAINTENANCE_TEMPLATE_START,
  MMSX_GET_DETAIL_MAINTENANCE_TEMPLATE_SUCCESS,
  MMSX_IN_ACTIVE_MAINTENANCE_TEMPLATE_FAILED,
  MMSX_IN_ACTIVE_MAINTENANCE_TEMPLATE_START,
  MMSX_IN_ACTIVE_MAINTENANCE_TEMPLATE_SUCCESS,
  MMSX_RESET_MAINTENANCE_TEMPLATE_STATE,
  MMSX_SEARCH_MAINTENANCE_TEMPLATE_FAILED,
  MMSX_SEARCH_MAINTENANCE_TEMPLATE_START,
  MMSX_SEARCH_MAINTENANCE_TEMPLATE_SUCCESS,
  MMSX_UPDATE_MAINTENANCE_TEMPLATE_FAILED,
  MMSX_UPDATE_MAINTENANCE_TEMPLATE_START,
  MMSX_UPDATE_MAINTENANCE_TEMPLATE_SUCCESS,
} from '../actions/maintenance-template'

const initialState = {
  isLoading: false,
  maintenanceTemplateList: [],
  maintenanceTemplateDetails: {},
  total: null,
}

export default function maintenanceTemplate(state = initialState, action) {
  switch (action.type) {
    case MMSX_SEARCH_MAINTENANCE_TEMPLATE_START:
    case MMSX_GET_DETAIL_MAINTENANCE_TEMPLATE_START:
    case MMSX_ACTIVE_MAINTENANCE_TEMPLATE_START:
    case MMSX_IN_ACTIVE_MAINTENANCE_TEMPLATE_START:
    case MMSX_CREATE_MAINTENANCE_TEMPLATE_START:
    case MMSX_UPDATE_MAINTENANCE_TEMPLATE_START:
      return {
        ...state,
        isLoading: true,
      }
    case MMSX_SEARCH_MAINTENANCE_TEMPLATE_FAILED:
    case MMSX_GET_DETAIL_MAINTENANCE_TEMPLATE_FAILED:
    case MMSX_ACTIVE_MAINTENANCE_TEMPLATE_SUCCESS:
    case MMSX_ACTIVE_MAINTENANCE_TEMPLATE_FAILED:
    case MMSX_IN_ACTIVE_MAINTENANCE_TEMPLATE_SUCCESS:
    case MMSX_IN_ACTIVE_MAINTENANCE_TEMPLATE_FAILED:
    case MMSX_CREATE_MAINTENANCE_TEMPLATE_SUCCESS:
    case MMSX_CREATE_MAINTENANCE_TEMPLATE_FAILED:
    case MMSX_UPDATE_MAINTENANCE_TEMPLATE_SUCCESS:
    case MMSX_UPDATE_MAINTENANCE_TEMPLATE_FAILED:
      return {
        ...state,
        isLoading: false,
      }
    case MMSX_SEARCH_MAINTENANCE_TEMPLATE_SUCCESS:
      return {
        ...state,
        maintenanceTemplateList: action.payload.list,
        isLoading: false,
        total: action.payload.total,
      }
    case MMSX_GET_DETAIL_MAINTENANCE_TEMPLATE_SUCCESS:
      return {
        ...state,
        maintenanceTemplateDetails: action.payload,
        isLoading: false,
      }
    case MMSX_RESET_MAINTENANCE_TEMPLATE_STATE:
      return {
        ...state,
        maintenanceTemplateDetails: {},
      }
    default:
      return state
  }
}
