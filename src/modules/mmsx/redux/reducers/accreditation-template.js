import {
  MMSX_ACTIVE_ACCREDITATION_TEMPLATE_FAILED,
  MMSX_ACTIVE_ACCREDITATION_TEMPLATE_START,
  MMSX_ACTIVE_ACCREDITATION_TEMPLATE_SUCCESS,
  MMSX_CREATE_ACCREDITATION_TEMPLATE_FAILED,
  MMSX_CREATE_ACCREDITATION_TEMPLATE_START,
  MMSX_CREATE_ACCREDITATION_TEMPLATE_SUCCESS,
  MMSX_GET_DETAIL_ACCREDITATION_TEMPLATE_FAILED,
  MMSX_GET_DETAIL_ACCREDITATION_TEMPLATE_START,
  MMSX_GET_DETAIL_ACCREDITATION_TEMPLATE_SUCCESS,
  MMSX_IN_ACTIVE_ACCREDITATION_TEMPLATE_FAILED,
  MMSX_IN_ACTIVE_ACCREDITATION_TEMPLATE_START,
  MMSX_IN_ACTIVE_ACCREDITATION_TEMPLATE_SUCCESS,
  MMSX_RESET_ACCREDITATION_TEMPLATE_STATE,
  MMSX_SEARCH_ACCREDITATION_TEMPLATE_FAILED,
  MMSX_SEARCH_ACCREDITATION_TEMPLATE_START,
  MMSX_SEARCH_ACCREDITATION_TEMPLATE_SUCCESS,
  MMSX_UPDATE_ACCREDITATION_TEMPLATE_FAILED,
  MMSX_UPDATE_ACCREDITATION_TEMPLATE_START,
  MMSX_UPDATE_ACCREDITATION_TEMPLATE_SUCCESS,
} from '../actions/accreditation-template'

const initialState = {
  isLoading: false,
  accreditationTemplateList: [],
  accreditationTemplateDetails: {},
  total: null,
}

export default function accreditationTemplate(state = initialState, action) {
  switch (action.type) {
    case MMSX_SEARCH_ACCREDITATION_TEMPLATE_START:
    case MMSX_GET_DETAIL_ACCREDITATION_TEMPLATE_START:
    case MMSX_ACTIVE_ACCREDITATION_TEMPLATE_START:
    case MMSX_IN_ACTIVE_ACCREDITATION_TEMPLATE_START:
    case MMSX_CREATE_ACCREDITATION_TEMPLATE_START:
    case MMSX_UPDATE_ACCREDITATION_TEMPLATE_START:
      return {
        ...state,
        isLoading: true,
      }
    case MMSX_SEARCH_ACCREDITATION_TEMPLATE_FAILED:
    case MMSX_GET_DETAIL_ACCREDITATION_TEMPLATE_FAILED:
    case MMSX_ACTIVE_ACCREDITATION_TEMPLATE_SUCCESS:
    case MMSX_ACTIVE_ACCREDITATION_TEMPLATE_FAILED:
    case MMSX_IN_ACTIVE_ACCREDITATION_TEMPLATE_SUCCESS:
    case MMSX_IN_ACTIVE_ACCREDITATION_TEMPLATE_FAILED:
    case MMSX_CREATE_ACCREDITATION_TEMPLATE_SUCCESS:
    case MMSX_CREATE_ACCREDITATION_TEMPLATE_FAILED:
    case MMSX_UPDATE_ACCREDITATION_TEMPLATE_SUCCESS:
    case MMSX_UPDATE_ACCREDITATION_TEMPLATE_FAILED:
      return {
        ...state,
        isLoading: false,
      }
    case MMSX_SEARCH_ACCREDITATION_TEMPLATE_SUCCESS:
      return {
        ...state,
        accreditationTemplateList: action.payload.list,
        isLoading: false,
        total: action.payload.total,
      }
    case MMSX_GET_DETAIL_ACCREDITATION_TEMPLATE_SUCCESS:
      return {
        ...state,
        accreditationTemplateDetails: action.payload,
        isLoading: false,
      }
    case MMSX_RESET_ACCREDITATION_TEMPLATE_STATE:
      return {
        ...state,
        accreditationTemplateDetails: {},
      }
    default:
      return state
  }
}
